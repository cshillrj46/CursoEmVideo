
def aumentar(preco):
    mais10 = (preco * 10) / 100
    novo = preco + mais10
    return novo

def diminuir(preco):
    menos13 = (preco * 13) / 100
    novo = preco - menos13
    return novo

def dobro(preco):
    mais2x = preco * 2
    return mais2x

def metade(preco):
    met = preco / 2
    return met
